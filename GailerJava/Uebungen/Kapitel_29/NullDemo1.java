package Kapitel_29;

public class NullDemo1 {

	public static void main(String[] args) {
		
		String a = "Zufaellige Notizen";
		String b = null;
		String c = "";
		
		if (a != null) {
			System.out.println(a);
		}
		
		if (b != null) {
			System.out.println(b);
		}
		
		if (c != null) {
			System.out.println(c);
		}

	}

}
